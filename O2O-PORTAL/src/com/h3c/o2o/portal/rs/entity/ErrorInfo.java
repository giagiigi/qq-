package com.h3c.o2o.portal.rs.entity;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * add description of types here
 *
 * @author j09980
 */
@XmlRootElement(name = "errorInfo")
@XmlAccessorType(XmlAccessType.FIELD)
public class ErrorInfo implements Serializable {

    /**
     * add description of field here
     */
    private static final long serialVersionUID = -6937316586844622272L;

    public ErrorInfo() {}

    public ErrorInfo(String error_code, String error_msg) {
    	this.error_code = error_code;
    	this.error_msg = error_msg;
    }

    private String error_code;

    private String error_msg;

    public String getError_msg() {
        return error_msg;
    }

    public void setError_msg(String error_msg) {
        this.error_msg = error_msg;
    }

    public String getError_code() {
        return error_code;
    }

    public void setError_code(String error_code) {
        this.error_code = error_code;
    }


}
